$('#recusar_radio_1').click(function () {
    if ($("input[type='radio'][id='recusar_radio_1']").is(':checked')) {
        $('#recusar_div').removeAttr('hidden');
    }
});
$('#aceitar_radio_1').click(function () {
    if ($("input[type='radio'][id='aceitar_radio_1']").is(':checked')) {
        $('#recusar_div').attr('hidden', true)
    }
});

